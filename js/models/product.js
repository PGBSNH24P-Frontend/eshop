export class Product {
    constructor(
        id,
        title,
        brand,
        category,
        description,
        price,
        images,
    ) {
        this.id = id;
        this.title = title;
        this.brand = brand;
        this.category = category;
        this.description = description;
        this.price = price;
        this.images = images;
    }
}